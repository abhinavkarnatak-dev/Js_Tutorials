// Loops or iterations

// for loop
for (let i = 1; i <= 10; i++) {
    const element = i;
    console.log(element);
}

console.log("---------------------------------------------");

//while loop
let i = 1
while (i<=10) {
    console.log(i);
    i++
}

console.log("---------------------------------------------");

//do-while loop
let j = 1
do {
    console.log(j);
    j++
} while (j<=10);